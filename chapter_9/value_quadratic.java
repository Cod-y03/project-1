

public class value_quadratic
{
    public static void main (String[] args )
    {
        double x = 10;

        System.out.println("x equals:" + ((3 * x * x) - (8 * x) + 4) );

        x = 12;

        System.out.println("x equals:" + ((3 * x * x) - (8 * x) + 4) );

        x = 8;

        System.out.println("x equals:" + ((3 * x * x) - (8 * x) + 4) );
    }
}